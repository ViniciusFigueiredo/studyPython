n1 = input("digite um numero: ")
n1 = int(n1)

n2 = input("digite outro numero: ")
n2 = int(n2)

if(n1==n2): #operado de igualdade
    print("o numero %d é igual a %d "%(n1, n2))
if(n1!=n2): #operador de diferença
    print("o numero %d é diferente de %d "%(n1, n2))
if(n1<n2): #operador menor que
    print("o numero %d é menor que %d "%(n1, n2))
if(n1>n2): #operador maior que
    print("o numero %d é maior que %d "%(n1, n2))
if(n1>=n2): #operador maior ou igual que
    print("o numero %d é maior ou igual que %d "%(n1, n2))
if(n1<=n2): #operador menor ou igual que
    print("o numero %d é menor ou igual que %d "%(n1, n2))
